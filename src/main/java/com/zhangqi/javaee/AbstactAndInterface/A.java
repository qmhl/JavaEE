package com.zhangqi.javaee.AbstactAndInterface;


/**
 * 众所周知普通类如果实现一个接口，那么普通类需要重写接口中的所有方法
 * 但是抽象类实现接口，可以完全覆盖/重写 接口中的方法，也可只重写接口中的某几个方法，
 * 子类再继承抽象类时，子类重写的方法即为抽象类中未重写接口中的方法。
 *
 * 在Java中，使用抽象类来实现接口，并不是毫无作用。相反，有时有很大的作用。
 *
 * 当你只想实现接口中的个别方法（不是所有方法）时，
 * 你可以先写一个抽象类来实现该接口，并实现除了你想要的方法之外的所有方法（方法体为空）。
 * 接着再用你的类继承这个抽象类，这个类中就只用实现你需要的方法了，这样就可以达到你的需要了。
 * 但是，如果你直接实现接口的话，你就需要实现接口的所有方法。
 */
public interface A {


    public void a();
    public void b();
    public void c();
}
